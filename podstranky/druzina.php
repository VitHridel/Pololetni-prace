<h2 id="druzina-nadpis">Družina</h2>
<p>
Školní družina je určena žákům 1. - 3. třídy. Ve školním roce 2020/2021 přijímáme do školní družiny i žáky z 4. ročníků. V případě vašeho zájmu o umístění dítěte do ŠD 
kontaktujte vedoucí školní družiny. Školní klub mohou navštěvovat všichni žáci naší školy. Školní  družina je v provozu denně od 6,30 - 18,00 hod. Vedoucí školní družiny je pí vychovatelka Bc. Jana Fingrová, DiS.
</p>


